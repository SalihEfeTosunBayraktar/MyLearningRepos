
radius = float(input("Daire Yarıçapını Giriniz : "))
mathPi=3.1415

circleArea = mathPi*(radius**2)
circleCircumference = 2*mathPi*radius

print("Dairenin Alanı : ",circleArea)
print("Dairenin Çevresi : ",circleCircumference)
print()

print("Alan : "+str(circleArea)+ " Çevre : "+str(circleCircumference))

